    private void gravaDados(int x) {
        System.out.println("Preparando para Gravar.");
        
        try {
            if (x > INCLUIR && jTxtId.getText().equals("")) {
                throw new SQLException("Informe um cadastro para continuar.");
            }
            
            lancamentos = new LancamentosTipo();
            LancamentosTipoDAO lancDAO = new LancamentosTipoDAO();
            
            try {
                lancamentos.setId(Integer.parseInt(jTxtId.getText()));
            } catch (Exception e) {
                lancamentos.setId(0);
            }
            
            lancamentos.setTitle(jTxtNome.getText());
            lancamentos.setDescription(jTxtDescricao.getText());
            lancamentos.setObrigaBem(jChbObriga.isSelected());
            lancamentos.setAntecipaPgto(jChbAntec.isSelected());
            
            switch (x) {
                
                case INCLUIR:
                    lancDAO.create(lancamentos);
                    jTxtId.setText(lancamentos.getId() + "");
                    
                    break;
                
                case ALTERAR:
                    lancDAO.update(lancamentos);
                    
                    break;
                
                case EXCLUIR:
                    System.out.println("Excluindo");
                    lancDAO.delete(lancamentos);
                    break;
            }
            
            lancDAO.c.commit();
            tools.DefaultMsg.saveDataSuccessfull();
            tools.ClearFields.ClearFields(jPanel1);
            
        } catch (Exception e) {
            conn.ConexaoMySQL.finalizarTransacao(false);
            tools.DefaultMsg.errorMsg(e.getMessage());
        }
        
    }
    
