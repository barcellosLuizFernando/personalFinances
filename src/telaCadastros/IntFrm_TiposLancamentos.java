    private void incluiPesquisa(int x) {
        switch (x) {
            case PESQUISA:
                jDial_PesquisaLancamentoTipo p = new jDial_PesquisaLancamentoTipo(this, true);
                p.setVisible(true);
                
                incluiPesquisa(lancamentos);
                
                break;
            
            case PESQUISA_ID:
                if (!"".equals(jTxtId.getText())) {
                    LancamentosTipoDAO tDAO = new LancamentosTipoDAO();
                    incluiPesquisa(tDAO.getLancamentosTipo(Integer.parseInt(jTxtId.getText())));
                }
                break;
        }
    }
    
    private void incluiPesquisa(LancamentosTipo lancamentos) {
        try {
            if (lancamentos.getId() == null) {
                throw new UnsupportedOperationException();
            }            
            
            jTxtId.setText(lancamentos.getId() + "");
            jTxtNome.setText(lancamentos.getTitle());
            jTxtDescricao.setText(lancamentos.getDescription());
            jChbAntec.setSelected(lancamentos.getAntecipaPgto());
            jChbObriga.setSelected(lancamentos.getObrigaBem());
        } catch (Exception e) {
            tools.ClearFields.ClearFields(jPanel1);
        }
    }
    
