    private void gravaDados(int action) {
        try {
            if (action > INCLUIR && jTxtId.getText().equals("")) {
                throw new SQLException("Informe um cadastro para continuar.");
            }
            
            EconomicGroup ct = new EconomicGroup();
            EconomicGroupDAO ctDAO = new EconomicGroupDAO();
            
            try {
                ct.setId(Integer.parseInt(jTxtId.getText()));
            } catch (Exception e) {
                ct.setId(0);
            }
            ct.setTitle(jTxtNome.getText());
            ct.setDescricao(jTxtDescricao.getText());
            
            switch (action) {
                case INCLUIR:
                    
                    ctDAO.create(ct);
                    jTxtId.setText(ct.getId() + "");
                    
                    break;
                
                case ALTERAR:
                    ctDAO.update(ct);
                    
                    break;
                
                case EXCLUIR:
                    ctDAO.delete(ct);
                    
                    break;
            }
            tools.DefaultMsg.saveDataSuccessfull();
            tools.ClearFields.ClearFields(jPanel1);
            
            conn.ConexaoMySQL.finalizarTransacao(true);
        } catch (Exception e) {
            tools.DefaultMsg.errorMsg(e.getMessage());
            conn.ConexaoMySQL.finalizarTransacao(false);
        }
        
    }
    
    private void incluiPesquisa(int x) {
        
        switch (x) {
            
            case PESQUISAR:
                jDial_PesquisaEconomicGroup d = new jDial_PesquisaEconomicGroup(this, true);
                d.setVisible(true);
                
                incluiPesquisa(economicGroup);
                
                break;
            
            case PESQUISAR_ID:
                
                if (!"".equals(jTxtId.getText())) {
                    EconomicGroupDAO ctDAO = new EconomicGroupDAO();
                    incluiPesquisa(ctDAO.getEconomicGroup(Integer.parseInt(jTxtId.getText())));
                }
                
                break;
            
        }
        
    }
    
    private void incluiPesquisa(EconomicGroup ct) {
        tools.ClearFields.ClearFields(jPanel1);
        
        try {
            if (ct.getId() == null) {
                throw new UnsupportedOperationException();
            }
            
            jTxtId.setText(ct.getId() + "");
            jTxtNome.setText(ct.getTitle());
            jTxtDescricao.setText(ct.getDescricao());
            
            
        } catch (Exception e) {
            
        }
    }
    
